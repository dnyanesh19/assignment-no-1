/*
Program to Find Quotient and Remainder
 */
public class division {
    public static void main(String[] args) 
    {
        int n1 = 11, n2 = 2;
        int q = n1 / n2;
        int r = n1 % n2;
        System.out.println("The quotient is: " + q);
        System.out.println("The remainder is: " + r);
    }
}
