/*
Write a program that reads a set of integers, and then prints the sum of 
the even and odd integers.
 */
public class oddeven_addition {
    public static void main(String args[])
    {
        int odd,even,sum=0;
        for(odd=1;odd<=10;odd=odd+2)
        {
            System.out.println(odd);
            
            
        }
        System.out.println(sum=sum+odd);
        for(even=0;even<=10;even=even+2)
        {
            System.out.println(even);
            
            
            
        }
        System.out.println(sum=sum+even);
        
    }
    
}
